public class LinePoint {
    public double x, y;

    public LinePoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public void setX(double x1) {
        x = x1;
    }

    public double getX() {
        return this.x;
    }

    public void setY(double y1) {
        y = y1;
    }

    public double getY() {
        return this.y;
    }
}
